    private void jButton2SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2SalvarActionPerformed
        if(flag==1){
        mod.setNome(jTextField1Nome.getText());
        mod.setEspecialidade((String) jComboBoxEspecialista.getSelectedValue());
        mod.setCrm(Integer.parseInt(jFormattedTextField1Crm.getText()));
        control.Salvar(mod);
        jTextField1Nome.setText("");
        jFormattedTextField1Crm.setText("");
        jTextField1Nome.setEnabled(false);
        jFormattedTextField1Crm.setEnabled(false);
        jComboBoxEspecialista.setEnabled(false);
        jButton2Salvar.setEnabled(false);
        }else{
         mod.setCodigo(Integer.parseInt(jtCOD.getText()));
         mod.setNome(jTextField1Nome.getText());
         mod.setEspecialidade((String)jComboBoxEspecialista.getSelectedValue());
         mod.setCrm(Integer.parseInt(jFormattedTextField1Crm.getText()));
         control.Editar(mod);
         jtCOD.setText("");
         jTextField1Nome.setText("");
         jFormattedTextField1Crm.setText("");
        jTextField1Nome.setEnabled(false);
        jFormattedTextField1Crm.setEnabled(false);
        jComboBoxEspecialista.setEnabled(false);
        jButton2Salvar.setEnabled(false);
        jButton1Novo.setEnabled(true);
        jButton3Cancelar.setEnabled(false);
        
        }
    }//GEN-LAST:event_jButton2SalvarActionPerformed

