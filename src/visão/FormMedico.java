    private void jButton6PesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6PesquisarActionPerformed
        mod.setPesquisa(jTextField1Pesquisa.getText());
        BeanslMedicos model = control.buscaMedicos(mod);
        jTextField1Nome.setText(model.getNome());
        jtCOD.setText(String.valueOf(model.getCodigo()));
        jFormattedTextField1Crm.setText(String.valueOf(model.getCrm()));
        jComboBoxEspecialista.setSelectedValue(model.getEspecialidade(), rootPaneCheckingEnabled);
        jButton4Editar.setEnabled(true);
        jButton5Excluir.setEnabled(true);
    }//GEN-LAST:event_jButton6PesquisarActionPerformed

